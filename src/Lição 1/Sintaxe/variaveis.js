// O tipo 'var' é usado para definir variáveis no JavaScript, mas é um método obsoleto para se declarar variáveis
var variavel1;

// O tipo 'const' é usado para definir variáveis com um valor inalterável, ele sempre permanecerá o valor inicial declarado. Se você não for mudar o valor da variável, defina-a como const
const variavel2 = 1;

// O tipo 'let' é usado para definir variáveis com um valor alterável, que em algum momento no código este valor irá mudar
let variavel3 = 1;
variavel3 = "texto";